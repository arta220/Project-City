using System;

namespace Domain.Finance
{
    /// <summary>
    /// Представляет финансовую транзакцию в системе
    /// Содержит информацию о денежной операции, её типе, категории и времени совершения
    /// </summary>
    public class Transaction
    {
      
            /// <summary>
            /// Получает или задает уникальный идентификатор транзакции
            /// </summary>
            /// <value>
            /// Глобально уникальный идентификатор (GUID), автоматически генерируемый при создании транзакции
            /// </value>
            public Guid Id { get; set; } = Guid.NewGuid();

            /// <summary>
            /// Получает или задает сумму транзакции
            /// </summary>
            /// <value>
            /// Величина денежных средств, участвующих в транзакции
            /// Положительное значение для доходов, отрицательное для расходов
            /// </value>
            public float Amount { get; set; }

            /// <summary>
            /// Получает или задает тип транзакции
            /// </summary>
            /// <value>
            /// Значение перечисления <see cref="TransactionType"/>, определяющее является ли транзакция доходом или расходом
            /// </value>
            public TransactionType Type { get; set; }

            /// <summary>
            /// Получает или задает категорию транзакции
            /// </summary>
            /// <value>
            /// Категория транзакции, например: "Налоги", "Инфраструктура", "Зарплаты" и т.д.
            /// </value>
            public string Category { get; set; }

            /// <summary>
            /// Получает или задает описание транзакции
            /// </summary>
            /// <value>
            /// Текстовое описание цели или причины транзакции
            /// </value>
            public string Description { get; set; }

            /// <summary>
            /// Получает или задает временную метку совершения транзакции
            /// </summary>
            /// <value>
            /// Время транзакции 
            /// </value>
            public long Timestamp { get; set; }
        }
}
